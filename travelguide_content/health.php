<h2 id="titulotexto">Health &amp; Safety</h2>
<p><b>Drinking water & Showers</b></br>
In Costa Rica tap water is safe to drink almost everywhere, however it is advisable to drink only bottled water when in doubt, specially in more rural areas. The best way to enjoy your flight and your vacation is to drink lots of water! At sea level, the human body loses about a quart of water a day through air expired from one&#39;s lungs and through the skin. At flight levels and higher desert and mountain elevations, this loss can be up to 100% greater. This water has to be replaced in order to avoid dehydration and its symptoms, so make sure you drink plenty of pure water
</p>