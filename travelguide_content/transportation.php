<h2 id="titulotexto">Transportation</h2>
<p>Costa Rica&#39;s offers a comprehensive network of travel options for visitor from the inexpensive and reliable public transport with bus services to just about every town and city, to very reliable shuttle buses operating on Seat-in basis and serving the main tourist destinations. Taxis are also plentiful and inexpensive, and in San Jose they are required to operate with meters for most trips. The quickest way to get around is to fly, and there are two main domestic airlines offer daily flights to most of the popular tourist destinations in Costa Rica. There are also plenty of car rental agencies, most of which rent four-wheel-drive vehicles.
</p>