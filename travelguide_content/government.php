<h2 id="titulotexto">Government</h2>
<p>Costa Rican is a democracy and its system of government is very similar to that of the United States of America. There are three branches of government: Executive, which consists of the president, two vice presidents and cabinet; the Legislative Assembly, with 57 individually elected deputies; and, the Judicial Branch, which consists of civil, criminal, appellate and constitutional courts. The President and members of the Legislative Assembly are elected for four-year terms and the president can&#39;t run for reelection.
</p>
