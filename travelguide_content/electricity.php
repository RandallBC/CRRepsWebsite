<h2 id="titulotexto">Electricity</h2>
<p>The Costa Rican electricity network is administrated by I.C.E. (<i>Instituto Costarricense de Electricidad</i>).
Despite its varied and rough terrain, electricity is available in 97% of the territory. Currently I.C.E. is implementing a pilot program to provide solar panels to homes which have no access to the conventional electricity network.
Standard current is 110volts, 60 Hertz and Costa Rica uses the exact same electrical socket standard as U.S.A. so adaptors are required.
