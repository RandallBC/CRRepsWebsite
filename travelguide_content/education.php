<h2 id="titulotexto">Education</h2>
<p>The Costa Rican government has long dedicated a significant portion of its national budget to education and other social services, a policy whose result has been a healthy and educated populace. The country has a literacy rate and average life expectancy that are much closer to those of Western European nations than most Latin American countries. Costa Rica has had a socialized medical system for nearly half a century, and while schools and clinics are found throughout the country, the Central Valley has several public universities and dozens of private universities. Travelers are more likely to encounter more educated people, and do not have to worry about most of the diseases they would expect to encounter in a tropical country. For those few travelers that do become sick or injured while in Costa Rica, there are hospitals and private clinics in San Jose provide a level of care comparable to that found in the United States, and for considerably less money.
</p>