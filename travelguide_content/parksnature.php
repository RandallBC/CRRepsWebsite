<h2 id="titulotexto">Parks &amp; Nature Reserves</h2>
<p>An astonishing 26 percent of Costa Rica is designated as national park, biological reserve, wildlife refuge, or some other category of protected area. Both private and public organisations work to protect this country of fascinating ecological diversity from the potential exploitation and ravages of other human beings. Below is a list of the most spectacular National Parks and Nature reserves in Costa:
</p>
<p><i>The most visited…</i></p>
<ul type=disk><li>Poas Volcano National Park (The most visited National Park, with a crater rim of 1.5 Km is one of the largest in the World)
</li>
<li>Manuel Antonio National is Costa Rica&#39;s second most visited park. Although Manuel Antonio National Park is Costa Rica&#39;s smallest national park, the diversity of wildlife in its 6.83 km² is unequaled with 109 species of mammals and 184 species of birds.
</li>
<li>Tortuguero National Park is the third-most visited park in Costa Rica, despite the fact that it can only be reached by airplane or boat. It is located in a very humid tropical climate encompassing a network of water canals and receives up to 250 inches (6,300 mm) of rain a year. Tortuguero&#39;s beaches are key nesting ground for endangered sea turtles, such as Hawksbill, Loggerheads, Green and Leatherbacks. 
</li>
</ul>
<p><i>The most Iconic…</i></p>
<ul type=disk><li>Arenal National Park is home to Arenal Volcano, Costa Rica&#39;s most active volcano which rises 1657 metres above sea level and was presumed extinct until 1968 when an earthquake caused it to erupt after approximately 400 years of dormancy. 
</li>
<li>Corcovado National Park is the last frontier and a wildlife Sanctuary considered one of the most biologically intense places on Earth by National Geographic.
</li>
<li>Chirripo National Park is home to the highest peak in Costa Rica with an altitude of 3,820 metres/12,532 feet.
</li>
<li>Cocos Island National Park is located approximately 550 km (340 mi) from the Pacific shore of Costa Rica. Cocos Island is a UNESCO World Heritage Site considered one of the best diving sites in the World.
</li>
<li>Guayabo National Monument covers the largest archaeological site in the country. Although still partly unexplored the area contains ancient bridges and dwellings, and petroglyphs have been found that are estimated at about 3,000 years old.
</li>
</ul>